# Tipos de Numeros
# Python tem varios "tipos" de numeros (literais numericos). Nós nos concentraremos 
# principalmente em numeros inteiros (int) e numeros de ponto flutuante (float)

# soma
print(1+1)
# subtração
print(2 - 1)

#multiplicação
print(2*2)

# divisão -> sempre retornam float
print(3/2)

# podemos especificar o tipo do numero
print(3.0/2)

# funciona tb para os outros 
print(3/2.0)

# Quadrado
print(3 ** 2)

# Raiz Quadrada
print(4 ** 0.5)

# módulo, resto de divisão
print(4 % 3)

# Ordem das operações em Python
print(2 + 10 * 10 + 3)

# Podemos usar parentesis para especificara ordem
print((2+10)*(10+3))

# Vamos criar um objeto qualquer e atribuí-lo o valor 5
a = 5
# Vamos somar os objetos
print(a + a)

# Redefinição de valores
a = 20
print(a)
print(a + a)


# Os nomes que você usa ao criar esses rótulos precisam seguir algumas regras: 
# 1. Os nomes não podem começar com um numero
# 2. Nçao pode haver espaços no nome, podemos usar o _ ou capitalização
# 3. Não é possível usar nenhum desses simbolos: "',<>/?|()!@#$%^&*~ç-+
# 4. É considerada a melhor prática (PEP8) que os nomes são minusculos
#
# Usar nomes especificos para as variáveis pode ser uma maneira muito útil de acompanhar diferentes variáveis em Python. Por Exemplo:
distancia = 100

tempo = 20

velocidade = distancia / tempo
print(velocidade)

# Use nomes de objetos para manter uma melhor relação com o que está acontecendo no seu código!
